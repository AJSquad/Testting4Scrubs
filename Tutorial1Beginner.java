/* Different data types:
1) Int: 32 bit value. Integral numbers only.
2) Short: 16 bit value. Holds smaller numbers that Int.
3) Long: Really big numbers (possibly bigger than Int, have to confirm).
4) Float: Short version of double. Requires 'f' at the end of the value.
5) Double: "Extra precision", essentially decimal numbers.
6) Char: Holds unicode characters e.g: 'A','c','e'
7) Boolean: True or False
8) Byte: Holds 8 bits of data. Negative or positive number with limits either end.
*/